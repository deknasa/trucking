          limit: 0,
          filters: JSON.stringify({
            "groupOp": "AND",
            "rules": [{
              "field": "statusaktif",
              "op": "cn",
              "data": "AKTIF"
            }]
          }),
          role: 'MARKETING'
        },
        success: response => {
          response.data.forEach(user => {
            let option = new Option(user.user, user.id)

            relatedForm.find(`[name="users[]"]`).append(option).trigger('change')
          });

          resolve()
        },
        error: error => {
          reject(error)
        }
      })
    })
  }

